package main;

public class App {

	public static void main(String[] args) {
		
		for (int i=67;i<88;i++) {
			char caracter = dameCaracterDe(i);
			System.out.println("Código Ascii: " + i 
					+ " Carácter: " + caracter);
		}
		
	}

	static char dameCaracterDe(int codigoAscii) {

		return (char) codigoAscii;
	}

}
