package principal;

import java.util.Scanner;

public class App {

	public static void main(String[] args) {
		
	
	
		System.out.println(factorial(0));
		
	}
	
	
	
	static int factorial (int numero) {
		
		int fact=1;
		for (int i = 1; i <=numero; i++) {
			fact*=i;  //factorial = factorial * i
		}
		return fact;
		
	}

}
